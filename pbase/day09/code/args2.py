# args2.py

def myfun(a, b, c):
	print("a-->",a)
	print("b-->",b)
	print("c-->",c)

# 位置傳參：
# myfun(100, 200, 300)

# 關鍵字傳參：
myfun(a=100, b=200, c=300)
myfun(c=30, b=20, a=10)
# myfun(a=1, c=3 ,b=5, d=10)

# 字典關鍵字傳參：

d1 = {"c":33, "a":11, "b":22}
myfun(**d1)

#說明：
#  字典傳參的鍵名和形參名必須一致
#  鍵名必須為字符串
#  鍵名要在形參中存在

